--Διαγραφή παλιών πινάκων
DROP TABLE  GENRE;
DROP TABLE  MOVIE;
DROP TABLE  FAVORITE_LIST;

--Πίνακας GENRE με κλειδί ID (αυτό που χρησιμοποιεί το themovieDB.org ) και όνομα 
CREATE TABLE GENRE (
GENRE_ID INTEGER PRIMARY KEY NOT NULL,
GENRE_NAME VARCHAR(20) NOT NULL);

--Πίνακας FAVORITE_LIST  κρατά τα δεδομένα των λίστεων του χρήστη 
CREATE TABLE FAVORITE_LIST(
FAVORITE_LIST_ID INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
FAVORITE_LIST_NAME VARCHAR (50) NOT NULL
);

--Πίνακας MOVIE  κρατά τα δεδομένα της ταινίας όπως αυτά είναι στο themovieDB.org 
--FK το είδος και η λίστα στα οποία ανήκει
CREATE TABLE MOVIE(
MOVIE_ID INTEGER PRIMARY KEY,
MOVIE_NAME VARCHAR(100) NOT NULL,
MOVIE_RELEASE_DATE DATE NOT NULL,
MOVIE_RATING FLOAT (10),
MOVIE_OVERVIEW VARCHAR (1000),
GENRE_ID_FK INT ,
CONSTRAINT GENRE_ID_FK FOREIGN KEY (GENRE_ID_FK)
        REFERENCES GENRE(GENRE_ID),
FAVORITE_LIST_ID_FK INT ,
CONSTRAINT FAVORITE_LIST_ID_FK FOREIGN KEY (FAVORITE_LIST_ID_FK)
        REFERENCES FAVORITE_LIST(FAVORITE_LIST_ID)
 );
